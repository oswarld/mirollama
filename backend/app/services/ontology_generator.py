def _to_pascal_case(name: str) -> str:
    """Convert an arbitrary formatted name to PascalCase (e.g. 'works_for' -> 'WorksFor', 'person' -> 'Person')"""
    # Split by non-alphanumeric characters
    parts = re.split(r'[^a-zA-Z0-9]+', name)
    # Then divide by camelCase boundaries (such as 'camelCase' -> ['camel', 'Case'])
    words = []
    for part in parts:
        words.extend(re.sub(r'([a-z])([A-Z])', r'\1_\2', part).split('_'))
    # Capitalize the first letter of each word, filter empty strings
    result = ''.join(word.capitalize() for word in words if word)
    return result if result else 'Unknown'


